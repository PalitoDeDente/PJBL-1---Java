public class Pet{
   private String nomePet;
   private String tipoPet;
   
   public Pet(String nomePet, String tipoPet){
      this.nomePet = nomePet;
      this.tipoPet = tipoPet; 
   }
   public String getnomePet(){
   return nomePet;
   }
   public String gettipoPet(){
   return tipoPet;
   }
}